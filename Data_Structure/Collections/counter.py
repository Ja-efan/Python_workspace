# Counter는 기본적으로 사칙연산을 지원한다.
# 덧셈,뺄셈,논리연산 가능

c4 = Counter(a=4,b=2,c=0,d=-2)
d = Counter(a=1,b=2,c=3,d=4)

c4.subtract(d) # c4-d
print("c4-d:",c4)

c5 = Counter(a=4,b=2,c=0,d=-2) 
print("c5+d:",c5+d)
print("c5&d:",c5&d)
print("c5|d:",c5|d)
